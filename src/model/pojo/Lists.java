package model.pojo;

public class Lists {
	private String bookid;
	private String bookname;
	private String bookauthor;
	private String bookindex;
	private String bookpress;
	private String regioname;
	private String bookposition;
	private int borrowable;
	private int booknum;
	public String getBookid() {
		return bookid;
	}
	public void setBookid(String bookid) {
		this.bookid = bookid;
	}
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public String getBookauthor() {
		return bookauthor;
	}
	public void setBookauthor(String bookauthor) {
		this.bookauthor = bookauthor;
	}
	public String getBookindex() {
		return bookindex;
	}
	public void setBookindex(String bookindex) {
		this.bookindex = bookindex;
	}
	public String getBookpress() {
		return bookpress;
	}
	public void setBookpress(String bookpress) {
		this.bookpress = bookpress;
	}
	public int getBorrowable() {
		return borrowable;
	}
	public void setBorrowable(int borrowable) {
		this.borrowable = borrowable;
	}
	public int getBooknum() {
		return booknum;
	}
	public void setBooknum(int booknum) {
		this.booknum = booknum;
	}
	public String getRegioname() {
		return regioname;
	}
	public void setRegioname(String regioname) {
		this.regioname = regioname;
	}
	public String getBookposition() {
		return bookposition;
	}
	public void setBookposition(String bookposition) {
		this.bookposition = bookposition;
	}
}
